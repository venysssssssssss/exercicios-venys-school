/*Crie um programa que verifique se um número é positivo ou negativo. */
public class ex12 {
    public static void main(String[] args) {
        int numero = -99;
        if(numero < 0){
            System.out.println("Esse número é negativo");
        }else{
            System.out.println("Esse número é positivo");
        }
    }
}
