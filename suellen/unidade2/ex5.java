/*Escreva um programa que verifique se um número é par ou ímpar. */

public class ex5 {
    public static void main(String[] args) {
        int valor = 7;
        
        if(valor % 2 == 0){
            System.out.println("O número é par");
        }else{
            System.out.println("O número é ímpar");
        }
    }
}
