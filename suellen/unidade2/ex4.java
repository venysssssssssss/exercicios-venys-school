/*Crie um programa que calcule a média de três notas.
- **Fórmula:** Média = (nota1 + nota2 + nota3) / 3 */

public class ex4 {
    public static void main(String[] args) {
        double n1, n2, n3;
        double media;
        n1 = 9;
        n2 = 10;
        n3 = 6.5;
        media = (n1 + n2 + n3)/3;

        System.out.println("Sua média é: "+ media);

    }
}
